# Identify the scores that are common to both teams (intersection on of sets)

# Define sets A and B 
A = {34, 56, 78, 90}
B = {78, 45, 90, 23}

# Find the intersection of sets A and B (common scores)
common_scores = A.intersection(B)

# Printing the common scores
print("Scores common to both teams (Intersection of sets A and B):", common_scores)
