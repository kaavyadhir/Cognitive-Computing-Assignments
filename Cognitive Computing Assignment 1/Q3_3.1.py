#  3.1 Write a Python program to check if a number is positive, negative, or zero 
# using an 
# if-else statement

# Input a number
num=float(input("Enter a no :"))

if num > 0:
    print("Positive number")

elif num < 0:
    print("Negative number")

else: 
    print("Number is zero")